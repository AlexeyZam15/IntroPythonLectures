# Арифметические операции
# +, -, *, /, %, //, **

# сложение +
a = +123
b = -321
c = a+b
print(c)

# вычитание -
a = 2
b = 8
c = a-b
print(c)

# умножение *
a = 2
b = 8
c = a*b
print(c)

# деление /
a = 12
b = 8
c = a/b
print(c)

# целочисленное деление //
a = 12
b = 8
c = a//b
print(c)

# остаток от деления %
a = 12
b = 8
c = a % b
print(c)

# возведение в степень **
a = 2
b = 8
c = a ** b
print(c)

# округление при умножении дробных чисел
a = 1.312312
b = 3
c = round(a * b, 5)
print(c)